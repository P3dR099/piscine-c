int	ft_sqrt(int nb)
{
	long int nb2;
	long int pw;
	long int i;

	nb2 = nb;
	i = 0;
	pw = 1;
	while (pw <= nb2)
	{
		i++;
		pw = i * i;
		if (pw == nb2)
			return (i);
	}
	return (0);
}
